package com.ly.building.model;

public class CalInfo {

    private String md5;

    private String json;

    private String deeledJson;

    private String threshold;

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getJson() {
        return json;
    }

    public void setJson(String json) {
        this.json = json;
    }

    public String getDeeledJson() {
        return deeledJson;
    }

    public void setDeeledJson(String deeledJson) {
        this.deeledJson = deeledJson;
    }

    public String getThreshold() {
        return threshold;
    }

    public void setThreshold(String threshold) {
        this.threshold = threshold;
    }
}
