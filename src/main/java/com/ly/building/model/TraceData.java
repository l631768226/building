package com.ly.building.model;

public class TraceData {

    private String md5;

    private String pathList;

    private String pointList;

    private String userPath;

    private String preData;

    private String proData;

    private String deeledPointData;

    private String deeledPathData;

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getPathList() {
        return pathList;
    }

    public void setPathList(String pathList) {
        this.pathList = pathList;
    }

    public String getPointList() {
        return pointList;
    }

    public void setPointList(String pointList) {
        this.pointList = pointList;
    }

    public String getUserPath() {
        return userPath;
    }

    public void setUserPath(String userPath) {
        this.userPath = userPath;
    }

    public String getPreData() {
        return preData;
    }

    public void setPreData(String preData) {
        this.preData = preData;
    }

    public String getProData() {
        return proData;
    }

    public void setProData(String proData) {
        this.proData = proData;
    }

    public String getDeeledPointData() {
        return deeledPointData;
    }

    public void setDeeledPointData(String deeledPointData) {
        this.deeledPointData = deeledPointData;
    }

    public String getDeeledPathData() {
        return deeledPathData;
    }

    public void setDeeledPathData(String deeledPathData) {
        this.deeledPathData = deeledPathData;
    }
}
