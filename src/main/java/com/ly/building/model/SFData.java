package com.ly.building.model;

public class SFData {

    private String md5;

    private String anaResult;

    private String pointMap;

    private String countMap;

    private String userList;

    private String periodInfo;

    public String getPeriodInfo() {
        return periodInfo;
    }

    public void setPeriodInfo(String periodInfo) {
        this.periodInfo = periodInfo;
    }

    public String getUserList() {
        return userList;
    }

    public void setUserList(String userList) {
        this.userList = userList;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getAnaResult() {
        return anaResult;
    }

    public void setAnaResult(String anaResult) {
        this.anaResult = anaResult;
    }

    public String getPointMap() {
        return pointMap;
    }

    public void setPointMap(String pointMap) {
        this.pointMap = pointMap;
    }

    public String getCountMap() {
        return countMap;
    }

    public void setCountMap(String countMap) {
        this.countMap = countMap;
    }
}
